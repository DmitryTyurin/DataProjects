# Напишите программу, которая принимает на вход три целых числа и выводит их среднее значение с точностью до одного знака после запятой.
# Округление сделайте через функцию round, либо любой другой. О ней подробно Вы узнаете в разделе 12.10.


def avg_int(numbers):
    return float(sum(numbers) / len(numbers))


print(round(avg_int([int(input()) for x in range(3)]), 2))


