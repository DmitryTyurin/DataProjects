# Датасет содержит записи с кодами ошибок.
# Напишите программу, которая принимает на вход общее количество записей и количество записей с ошибками, а затем выводит процент ошибок в датасете.

all_data = int(input())
error_data = int(input())

percent_error = error_data / all_data * 100

print(f"Процент ошибок: {percent_error} %")


# В журнале системы указано время выполнения задач в секундах.
# Напишите программу, которая принимает на вход количество секунд и преобразует его в часы, минуты и секунды.

seconds = int(input())

remaining_seconds = seconds % 3600
hours = seconds // 3600
minutes = remaining_seconds // 60
seconds = remaining_seconds % 60

print(f"Время выполнения: {hours} час {minutes} минута {seconds} секунд")


