# Напишите программу, которая принимает число и проверяет, является ли оно простым. Используйте цикл for для проверки делителей числа.

num = int(input())


def is_prime(n):
    if n <= 1:
        return "не является"
    for i in range(2, n):
        if n % i == 0:
            return "не является"
    return "является"


print(f"Число {num} {is_prime(num)} простым.")


