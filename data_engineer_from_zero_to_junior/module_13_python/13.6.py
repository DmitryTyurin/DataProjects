# Напишите программу, которая принимает на вход целое число и находит наибольшую непрерывную последовательность одинаковых цифр.
# Программа должна выводить цифру и длину этой последовательности.
# Используйте только if-else и while (без строк, списков и функций обработки строк).

num = int(input())
max_digit = 0
max_length = 0
current_digit = -1
current_length = 0

while num > 0:
    digit = num % 10
    if digit == current_digit:
        current_length += 1
    else:
        current_digit = digit
        current_length = 1
    if current_length > max_length:
        max_digit = current_digit
        max_length = current_length
    num //= 10

print(f"Цифра: {max_digit}, Длина последовательности: {max_length}")
