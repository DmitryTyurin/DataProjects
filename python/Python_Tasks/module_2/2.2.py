# Палиндром – это слово или фраза, которые читаются одинаково слева направо и справа налево. Например, "мадам", "казак", "шалаш".


def is_palindrome(word):
    word = word.lower()

    if word == word[::-1]:
        print("YES")
    else:
        print("NO")


is_palindrome(input())


