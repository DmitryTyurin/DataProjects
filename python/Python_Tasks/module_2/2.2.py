# На вход подается строка. Определите, является ли эта строка "правильной" с точки зрения регистра букв:
# • Первая и последняя буквы должны быть в верхнем регистре.
# • Все остальные буквы должны быть в нижнем регистре.
# Выведите True, если строка соответствует этому правилу, иначе выведите False.


def is_valid_string(s):
    if len(s) < 2:
        return False

    if not s[0].isupper() or not s[-1].isupper():
        return False

    for char in s[1:-1]:
        if not char.islower():
            return False

    return True


print(is_valid_string(input()))
